                                    <h4 class="text-primary mb-20 subsection-heading font-weight-bold">戴口罩</h4>
                                    <p>空气传播是新冠病毒的主要传播方式。而戴口罩能有效阻止新冠病毒的人际传播。
                                        口罩的分类有很多种包括纱布口罩、医用口罩、防尘口罩、防油口罩、N95口罩等。但是只有医用口罩和n95口罩预防新冠肺炎。医用口罩的面体部分可分为内中外三层，内层为亲肤材质，中层为隔离层，外层为特殊材料抑菌层，医用口罩能够阻隔微小的病毒气溶胶。N95口罩能够阻隔直径0.075微米的微粒，且成功率能够达到95%以上。</p>
                                    <p class="mb-4">
                                        保证口罩完全罩住口鼻。调整口罩位置，正确覆盖范围是下巴至眼睛下方1CM。摘口罩的时候，尽量不要触摸口罩的外侧，摘下口罩后记得立即洗手。一次性医用口罩不要重复使用。一旦口罩破了或者脏了，需要立即更换新的口罩。</p>
