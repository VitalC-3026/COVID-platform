                                    <h4 class="text-primary mb-20 subsection-heading font-weight-bold">勤洗手</h4>
                                    <p>
                                        人们在触摸了被喷嚏或鼻涕污染的物品如桌子、门把手、键盘、钢笔等，再接触口、鼻就有可能被传染。有些人经常无意识地抚摸脸部，如抠鼻子、用手擦眼，那就非常危险。眼部通过鼻泪管与鼻腔相通，因而可能被传染。
                                        因此，经常洗手有助于减少感染病毒的机率。洗手一般可将皮肤角化上皮 60% ～ 90%的微生物消除，若结合刷洗，微生物清除率可达 90% ～
                                        98%，因此，洗手是最简单、最有效的预防传染病传播的方法。</p>
                                    <p class="mb-4">洗手要规范，正确的洗手方法是使用流动的清水，使用香皂或洗手液后反复揉搓冲洗。然后用干净的个人专用毛巾或一次性纸巾擦干；
                                        双手清洗后，应以纸巾包裹水龙头或清水冲净水龙头再关闭；在洗手间洗手后最好用擦手纸巾包住门把手，开门后再将擦手纸扔掉。</p>
