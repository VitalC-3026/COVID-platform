                                    <h4 class="text-primary mb-20 subsection-heading font-weight-bold">避免密切接触</h4>
                                    <p>
                                        尽量避免与患者或者疑似接触者近距离接触；尽量避免在未加防护情况下与养殖或野生动物近距离接触；尽量避免与生病的动物和变质的肉接触；尽量避免与生鲜市场里的流浪动物、垃圾废水接触</p>
                                    <p class="mb-4">与发病病例（疑似和确诊病例）有如下接触情形之一者属于密切接触者：
                                        （1）与病例共同居住、学习、工作或其他有密切接触的人员；
                                        （2）诊疗、护理、探视病例时未采取有效防护措施的医护人员、家属或其他与病例有类似近距离接触的人员；
                                        （3）病例同病室的其他患者及其陪护人员；
                                        （4）与病例乘坐同一交通工具并有近距离接触人员；
                                        （5）现场调查人员调查经评估认为符合条件的人员。</p>
